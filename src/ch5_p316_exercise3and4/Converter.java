package ch5_p316_exercise3and4;

import java.util.Scanner;

abstract class Converter {
	abstract protected double convert(double src); // 추상 메소드
	abstract protected String getSrcString(); // 추상 메소드
	abstract protected String getDestString(); // 추상 메소드
	protected double ratio; // 비율을 나타낼 변수 ratio.
	
	public void run() {
		Scanner scanner = new Scanner(System.in);
		System.out.println(getSrcString() + "을 " + getDestString() + "로 바꿉니다."); //어 왜 '랑 "랑 기능 다르냐? 왜 ' 하면 에러 뜨냐? 나중에 알아보자.
		System.out.print(getSrcString() + "을 입력하세요>> ");
		
		double val = scanner.nextDouble();
		double res = convert(val);
		
		System.out.println("변환 결과: " + res + getDestString() + "입니다. ㅋ"); 
		scanner.close();
	}
	
}
